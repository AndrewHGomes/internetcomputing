<h2>Sistemas</h2>

<Audios modulo="M3" aulas={["01", "02", "03", "04"]} />

<p>
  A introdução ao estudo de sistemas propõe um resgate da curiosidade
  investigativa para compreender a <strong>visão sistêmica</strong>. Esse
  conceito consiste em observar a razão das coisas e as conexões entre elas,
  permitindo abstrair o conhecimento comum para enxergar a complexidade oculta
  em tarefas aparentemente simples. Ao adotar essa perspectiva, facilita-se a
  compreensão de como pequenas ações repercutem em estruturas maiores.
</p>

<p>
  Um <strong>sistema</strong> é definido como um conjunto de elementos,
  concretos ou abstratos, que estão intelectualmente organizados. O
  <strong>elemento</strong> é considerado a menor parte integrante desse sistema.
  Embora o termo seja frequentemente associado ao mundo virtual, ele deve ser compreendido
  de forma ampla, aplicando-se a ambientes naturais, sociais, biológicos ou administrativos.
</p>

<p>
  A análise sistêmica revela que nenhuma tarefa ocorre de forma isolada,
  existindo um emaranhado de ligações fundamentais:
</p>

<ul>
  <li>
    <strong>Dependências</strong>: tarefas ou elementos que precisam estar
    prontos ou disponíveis antes do início de uma ação.
  </li>
  <li>
    <strong>Dependentes</strong>: atividades que só podem ser executadas após a
    conclusão de uma etapa anterior.
  </li>
</ul>

<p>
  No contexto da <strong>administração</strong>, essa lógica é essencial para
  entender a relação entre todas as atividades desenvolvidas em uma organização.
  Ao analisar processos, como o simples ato de respirar ou lavar a louça,
  percebe-se que cada conjunto depende de outros conjuntos menores e maiores
  para funcionar, formando uma estrutura de mútua necessidade.
</p>

<h3>Teoria dos Sistemas</h3>

<p>
  A <strong>Teoria Geral de Sistemas (TGS)</strong>, desenvolvida pelo biólogo
  Ludwig von Bertalanffy entre 1950 e 1968, surgiu como uma crítica à
  <strong>lógica reducionista</strong>. O método reducionista dividia o
  conhecimento em partes isoladas, o que gerava esforços redundantes e impedia a
  visão do todo. A TGS propõe, em vez disso, uma
  <strong>perspectiva integrada</strong>, onde áreas como física, biologia e
  administração são analisadas de forma interdependente.
</p>

<p>
  O conceito central da TGS define o <strong>sistema</strong> como um conjunto
  de elementos ou componentes que interagem entre si para atingir objetivos
  comuns. A premissa fundamental é que os elementos, quando analisados
  isoladamente, perdem propriedades que só se manifestam no conjunto. Assim, o
  sistema é caracterizado pelas suas unidades e pelas
  <strong>inter-relações mútuas</strong> que estabelecem entre si.
</p>

<p>
  Na <strong>administração</strong>, a TGS ganhou força com o avanço tecnológico
  e a necessidade de controle de processos. A teoria não foca em soluções
  práticas imediatas, mas em fornecer <strong>conceitos fundamentais</strong>
  que permitam entender a organização como um organismo vivo. A eficácia administrativa
  depende da compreensão de que a informação gerada em um setor possui um
  <strong>vínculo</strong> direto com os demais, e sua leitura isolada pode omitir
  dados cruciais para a tomada de decisão.
</p>

<ul>
  <li>
    <strong>Elementos básicos</strong>: as menores unidades que compõem o
    sistema.
  </li>
  <li>
    <strong>Interdependência</strong>: a necessidade mútua entre as partes para
    o funcionamento do todo.
  </li>
  <li>
    <strong>Objetivos</strong>: o propósito final que orienta a interação dos
    componentes.
  </li>
</ul>

<h3>Entrada, Processamento e Saída</h3>

<p>
  O funcionamento básico de qualquer sistema fundamenta-se em um ciclo de
  transformação que conecta a organização ao seu meio. Esse ciclo é composto por
  três etapas principais:
</p>

<ul>
  <li>
    <strong>Entradas (Inputs)</strong>: São os recursos ou informações que
    alimentam o sistema, como matérias-primas, energia, capital, dados ou
    pessoas.
  </li>
  <li>
    <strong>Processamento</strong>: É a fase de transformação, onde as entradas
    são trabalhadas para atingir o propósito do sistema. Sua eficiência depende
    da qualidade dos insumos recebidos.
  </li>
  <li>
    <strong>Saída (Output)</strong>: É o resultado final, que pode se manifestar
    na forma de produtos, serviços ou informações, devendo sempre estar alinhado
    ao objetivo inicial.
  </li>
</ul>

<h4>Classificação dos Sistemas</h4>

<p>
  Os sistemas são categorizados de acordo com o nível de interação que possuem
  com o ambiente externo:
</p>

<ul>
  <li>
    <strong>Sistemas Abertos (Orgânicos)</strong>
    Caracterizam-se por uma forte relação de causa e efeito com o meio exterior.
    Eles possuem uma diversidade de entradas e saídas que nem sempre são previsíveis,
    exigindo <strong>flexibilidade</strong> e capacidade de adaptação. As
    <strong>empresas</strong> são o exemplo clássico de sistemas abertos, pois dependem
    de fatores externos como legislação, economia, fornecedores e clima para funcionar
    e precificar seus produtos.
  </li>
  <li>
    <strong>Sistemas Fechados (Mecânicos ou Determinísticos)</strong>
    Apresentam pouca ou nenhuma interação com o ambiente externo. Suas entradas e
    saídas são limitadas e bem definidas, o que gera um
    <strong>comportamento-padrão</strong> e previsível. Máquinas e equipamentos tecnológicos
    são exemplos comuns, pois operam em um estado de relativo isolamento em relação
    ao contexto social ou ambiental.
  </li>
</ul>

<h4>Impácto na Administração</h4>

<p>
  A <strong>Teoria Geral de Sistemas (TGS)</strong> revolucionou a administração
  ao introduzir a visão da organização como um sistema aberto. Anteriormente, as
  teorias clássicas (Taylor e Fayol), humanistas e burocráticas focavam quase
  exclusivamente na <strong>eficiência interna</strong>. A TGS mudou esse
  paradigma, estabelecendo que a sobrevivência de uma organização depende da sua
  total interação e ajuste com o ambiente externo.
</p>

<h3>Aplicando a Visão de Sistemas</h3>

<p>
  A aplicação da <strong>visão sistêmica</strong> exige o exercício de "diminuir
  o zoom", permitindo observar processos de forma ampla em vez de focar em dados
  isolados. Na administração, essa perspectiva é vital porque informações
  fragmentadas podem levar a conclusões errôneas e decisões ineficazes. O
  sucesso no desenvolvimento de sistemas e na gestão depende dessa capacidade de
  conectar diferentes variáveis para enxergar a realidade operacional. O exemplo
  dos custos logísticos ilustra como a <strong>integração de dados</strong> altera
  a percepção dos fatos:
</p>

<ul>
  <li>
    <strong>Dados Isolados</strong>: Ao analisar apenas o gasto com combustível,
    um veículo pode parecer ineficiente. Ao analisar apenas o peso transportado,
    outro pode parecer ocioso.
  </li>
  <li>
    <strong>Análise Sistêmica</strong>: Ao cruzar as informações (custo de
    combustível versus peso transportado), revela-se o
    <strong>custo por peso transportado</strong>.
  </li>
</ul>

<p>
  Essa visão de conjunto demonstra que o veículo que consome mais combustível
  pode ser, na verdade, o mais produtivo por quilo carregado. Portanto, os <strong
    >elementos isolados</strong
  > refletem uma parcialidade que o sistema, quando lido como um todo, desmente.
</p>

<h4>Síntese Prática</h4>

<p>Para internalizar esse conceito, deve-se considerar que:</p>

<ul>
  <li>
    Informações de diferentes setores (como pagamentos e logística) são <strong
      >interdependentes</strong
    >.
  </li>
  <li>
    A tomada de decisão baseada em um único elemento ignora a complexidade do <strong
      >sistema orgânico</strong
    >.
  </li>
  <li>
    A visão sistêmica transforma dados brutos em <strong
      >informação estratégica</strong
    > coerente.
  </li>
</ul>

<h3>Tipificando o Sistema</h3>

<p>
  A classificação de um sistema é determinada pelo nível de influência que o <strong
    >ambiente externo</strong
  > exerce sobre ele. Independentemente de ser aberto ou fechado, o funcionamento
  estrutural permanece o mesmo: todos dependem da tríade entrada, processamento e
  saída para cumprir sua finalidade.
</p>

<h4>Sistemas Abertos vs Sistemas Fechados</h4>

<ul>
  <li>
    <strong>Sistemas Abertos</strong>: São influenciados por demandas externas
    voláteis. O exemplo da produção agrícola demonstra que, embora existam
    etapas claras (plantar, regar, colher), fatores como clima e economia
    alteram o resultado.
  </li>
  <li>
    <strong>Sistemas Fechados</strong>: Não sofrem interferência externa em seus
    resultados. Uma <strong>calculadora</strong> ou um software específico são
    exemplos claros; o cálculo "2+2" resultará em "4" independentemente de
    crises econômicas ou mudanças climáticas, seguindo um comportamento
    <strong>determinístico</strong>.
  </li>
</ul>

<h4>O Ciclo de Funcionamento</h4>

<p>Todo sistema, para ser útil, deve seguir um fluxo lógico de execução:</p>

<ol>
  <li>
    <strong>Entradas (Inputs)</strong>: Representam toda a informação ou
    material necessário. No sistema fechado (calculadora), são os números e
    operações; no sistema aberto (agricultura), são as sementes e insumos.
  </li>
  <li>
    <strong>Processamento</strong>: É a execução de
    <strong>regras preestabelecidas</strong>. Na tecnologia, são algoritmos e
    sequências matemáticas; na natureza ou administração, são fluxos de trabalho
    e processos biológicos que transformam a entrada.
  </li>
  <li>
    <strong>Saída (Output)</strong>: É a <strong>finalidade</strong> ou o produto
    final do sistema. A saída de um sistema (como a colheita) serve frequentemente
    como entrada para outro sistema (como uma indústria), evidenciando a interconexão
    sistêmica.
  </li>
</ol>

<p>
  A saída é o que realmente justifica a existência do sistema, sendo o resultado
  esperado para resolver um problema ou atender a uma necessidade específica.
</p>

<h3>Conclusão</h3>

<p>
  Esta conclusão reforça os conceitos fundamentais para a internalização da <strong
    >visão sistêmica</strong
  >
  e a aplicação prática do conhecimento adquirido. Um <strong>sistema</strong> é
  consolidado como um conjunto de elementos intelectualmente organizados, podendo
  ser classificado em três grandes esferas:
</p>

<ul>
  <li>
    <strong>Sistemas Naturais</strong>: Compostos por elementos orgânicos e
    fisiológicos.
  </li>
  <li>
    <strong>Sistemas Sociais</strong>: Envolvem organizações, regras, leis e
    interações humanas.
  </li>
  <li>
    <strong>Sistemas Virtuais</strong>: Abrangem o universo de softwares,
    algoritmos e aplicativos.
  </li>
</ul>

<p>
  A gestão da complexidade é feita através da <strong
    >subdivisão do sistema</strong
  >
  em partes menores, facilitando sua administração sem perder o foco na finalidade
  principal. A <strong>visão sistêmica</strong> é a ferramenta que permite antecipar
  as implicações de cada solução em diferentes contextos, evitando que a resolução
  de um problema gere impactos negativos em sistemas correlatos.
</p>

<p>
  A qualidade da <strong>saída</strong> (o resultado) depende diretamente da
  qualidade da <strong>entrada</strong>. No processo de análise, eleger as
  perguntas corretas é fundamental para garantir que a informação de entrada
  seja precisa e relevante para o processamento.
</p>

<p>
  Por fim, a <strong>tipificação</strong> do sistema permanece atrelada à sua relação
  com o meio:
</p>

<ul>
  <li>
    <strong>Sistema Aberto</strong>: Suscetível a interferências e trocas com o
    ambiente externo.
  </li>
  <li>
    <strong>Sistema Fechado</strong>: Isolado de influências externas, mantendo
    um funcionamento autônomo e previsível.
  </li>
</ul>
