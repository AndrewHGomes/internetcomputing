<h2>Gerenciamento das Informações</h2>

<Audios modulo="M3" aulas={["21", "22", "23", "24"]} />

<p>
  A gestão das informações é essencial para apoiar decisões organizacionais e
  estratégias de negócio. O uso de sistemas informatizados e tecnologias da
  informação permite coletar, organizar, analisar e proteger dados relevantes
  para a organização.
</p>

<h3>Obsolescência Programada</h3>

<p>
  Obsolescência programada é a prática de desenvolver produtos com vida útil
  previamente definida, principalmente os tecnológicos, fazendo com que se
  tornem obsoletos e precisem ser substituídos por modelos mais novos.
</p>

<p>
  Essa obsolescência ocorre quando produtos deixam de atualizar, tornam-se
  lentos ou apresentam falhas, como desgaste de bateria. Está associada à lógica
  de faturamento e lucro, mas também impulsiona o avanço tecnológico.
</p>

<h4>Ciclo de vida do produto</h4>

<p>
  O produto passa pelas fases de introdução no mercado, crescimento das vendas,
  maturidade e declínio. Quando o crescimento se estabiliza ou diminui, um novo
  modelo é lançado. Para impulsionar as vendas do novo produto, o modelo
  anterior é descontinuado, caracterizando a obsolescência programada.
</p>

<h4>Impactos da obsolescência na gestão</h4>

<ul>
  <li>Uso estratégico da obsolescência em produtos e serviços.</li>
  <li>Necessidade de substituição de equipamentos, sistemas e ferramentas.</li>
  <li>
    Implantação constante de novos sistemas devido à evolução tecnológica.
  </li>
</ul>

<h4>Tecnologias relacionadas ao uso da informação</h4>

<ul>
  <li>
    <strong>Cloud computing:</strong> uso de recursos computacionais, como armazenamento
    e processamento, disponibilizados pela internet sob demanda.
  </li>
  <li>
    <strong>Big data:</strong> grande volume de dados captados continuamente, permitindo
    análise e interpretação estratégica das informações.
  </li>
  <li>
    <strong>Business intelligence:</strong> processo de coleta, organização, análise,
    compartilhamento e monitoramento de informações para apoiar a gestão e a tomada
    de decisão.
  </li>
</ul>

<h4>Sistemas de informação</h4>

<p>
  Os sistemas de informação são fundamentais para o gerenciamento das
  informações, pois organizam dados e os transformam em conhecimento útil para
  decisões estratégicas e operacionais.
</p>

<h3>Tecnologia da Informação e Comunicação (TIC)</h3>

<p>
  Um sistema de informação pode ser manual ou informatizado. Sua função é
  coletar, organizar e transmitir dados, permitindo análise e uso das
  informações na tomada de decisões, com impacto direto nos resultados
  organizacionais.
</p>

<p>
  As TICs integram hardware, software e elementos de comunicação, formando a
  base das tecnologias da informação utilizadas nas organizações e na área
  educacional.
</p>

<h4>Papel das TICs nos sistemas de informação</h4>

<p>
  As TICs permitem a transição dos sistemas manuais para sistemas
  informatizados, oferecendo maior agilidade, capacidade de processamento e
  volume de transações de dados. Elas funcionam como a plataforma tecnológica de
  suporte aos sistemas de informação.
</p>

<h4>Estrutura das TICs</h4>

<p>As TICs seguem a lógica de sistemas com:</p>

<ul>
  <li>Entrada de dados</li>
  <li>Processamento</li>
  <li>Saída de informações</li>
</ul>

<h4>No processamento estão envolvidos:</h4>

<ul>
  <li>Hardware (equipamentos)</li>
  <li>Software</li>
  <li>Banco de dados</li>
  <li>Redes de dados</li>
</ul>

<h4>Camadas do software</h4>

<p>O software é composto por três camadas principais:</p>

<ul>
  <li>
    <strong>Interface do usuário:</strong> meio de interação entre usuário e máquina,
    como telas, botões, teclado, áudio e vídeo.
  </li>
  <li>
    <strong>Código:</strong> conjunto de instruções programadas que definem as ações
    e reações do sistema.
  </li>
  <li>
    <strong>Banco de dados:</strong> estrutura lógica de armazenamento que organiza
    os dados e permite acesso conforme o tipo de informação.
  </li>
</ul>

<h4>Sistemas manuais e automatizados</h4>

<p>
  A presença das TICs caracteriza os sistemas automatizados, diferenciando-os
  dos sistemas manuais pela <strong>
    eficiência, rapidez e capacidade de tratamento das informações
  </strong>.
</p>

<h3>Objetivos dos Sistemas de Informação</h3>

<p>
  Os sistemas de informação nas organizações têm como objetivo apoiar a gestão e
  melhorar os resultados organizacionais. Segundo Laudon e Laudon (2010), eles
  atendem a cinco objetivos principais:
</p>

<ul>
  <li>Atingir a excelência operacional.</li>
  <li>Desenvolver novos produtos e serviços.</li>
  <li>Melhorar a qualidade das decisões gerenciais.</li>
  <li>Aprimorar o relacionamento com os clientes.</li>
  <li>Assegurar a sustentabilidade do negócio.</li>
</ul>

<p>
  Esses objetivos envolvem apoio às <strong>atividades operacionais</strong>, ao
  <strong>desenvolvimento</strong>
  e <strong>melhoria de produtos e serviços</strong> e ao fornecimento de
  informações para <strong>decisões gerenciais mais eficazes</strong>.
</p>

<p>
  Os sistemas também contribuem para o relacionamento com os clientes, ao
  agilizar processos e fornecer informações que permitem melhor atendimento.
  Dessa forma, auxiliam na sustentabilidade do negócio, considerando aspectos
  econômicos, ambientais e sociais ao longo do tempo.
</p>

<h4>Tipos de sistemas de informação quanto à abrangência</h4>

<p>
  Os sistemas de informação podem ser classificados em departamentais ou
  integrados.
</p>

<ul>
  <li>
    <strong>Sistemas departamentais:</strong> atendem a funções específicas de um
    único departamento, gerando informações voltadas apenas às necessidades desse
    setor, como um sistema de cadastro de clientes para a área comercial.
  </li>
  <li>
    <strong>Sistemas integrados:</strong> interligam diversos departamentos, permitindo
    o compartilhamento de informações entre áreas. Um exemplo é o sistema de controle
    de estoque integrado aos setores financeiro, comercial e de produção, apoiando
    pagamentos, vendas e planejamento produtivo.
  </li>
</ul>

<h3>Sistemas de Informação Gerencial</h3>

<p>
  Os sistemas de informação possuem diferentes denominações porque atendem a
  funções distintas dentro das organizações, assim como as pessoas desempenham
  funções diferentes.
</p>

<h4>SPT – Sistemas de Processamento de Transações</h4>

<p>
  Os SPTs têm <strong>função operacional</strong>. Eles
  <strong>
    registram e processam as transações rotineiras da organização
  </strong>, sem foco analítico ou gerencial. Exemplos de SPTs:
</p>

<ul>
  <li>Processamento de folha de pagamento.</li>
  <li>Registro de vendas de produtos ou serviços.</li>
  <li>Emissão de notas fiscais eletrônicas.</li>
</ul>

<h4>SIG – Sistemas de Informação Gerencial</h4>

<p>
  Os SIGs <strong>organizam os dados coletados pelos SPTs</strong> e transformam
  esses dados em <strong>informações úteis</strong> para o gerenciamento da
  organização. Seu foco é
  <strong>apoiar o controle, a análise e a tomada de decisões gerenciais</strong
  >.
</p>

<p>
  A eficiência dos SIGs depende da integração entre os diversos SPTs, pois
  quanto maior o volume e a qualidade das transações registradas, melhores serão
  as informações gerenciais geradas.
</p>

<h4>ERP – Enterprise Resource Planning</h4>

<p>
  O ERP é um <strong>sistema que integra dados e processos</strong> de toda a organização
  em uma única plataforma, permitindo o planejamento e o gerenciamento dos recursos
  empresariais.
</p>

<p>
  O funcionamento do ERP ocorre por meio de módulos que atendem aos principais
  setores da organização. Essa estrutura integrada oferece ao gestor uma visão
  global do negócio.
</p>

<p>
  A lógica do ERP se <strong>baseia no processamento das transações</strong> (SPTs)
  e na geração de informações gerenciais (SIGs), unificando esses elementos em um
  sistema integrado.
</p>

<h3>Sistemas para Estratégia e Decisão</h3>

<p>
  Os SIGs utilizam <strong>dados dos diversos setores</strong> da organização
  para gerar informações que
  <strong>apoiam a gestão, a análise e o planejamento</strong>.
</p>

<h4>Relatórios gerenciais</h4>

<p>
  As informações gerenciais são apresentadas por meio de relatórios gerenciais,
  que reúnem <strong>
    dados atualizados e confiáveis, organizados de forma clara e objetiva
  </strong>. Esses relatórios permitem análise, controle e planejamento de
  ações.
</p>

<p>
  As informações também podem ser apresentadas em gráficos, facilitando a
  visualização e a identificação dos principais pontos de atenção.
</p>

<h4>Tipos de relatórios gerenciais</h4>

<ul>
  <li>
    <strong>Programados:</strong> relatórios periódicos, como demonstrativos diários
    de vendas ou relatórios financeiros mensais.
  </li>
  <li>
    <strong>Sob demanda:</strong> relatórios solicitados conforme necessidades específicas,
    como histórico de pagamentos de um fornecedor ou vendas de um produto em determinado
    período.
  </li>
  <li>
    <strong>Por exceção:</strong> relatórios gerados quando ocorre algo fora do padrão
    esperado, como queda na produção ou anomalias em processos, auxiliando na tomada
    de ações corretivas.
  </li>
</ul>

<h4>Sistemas de Informação Estratégica (SIS)</h4>

<p>Os SIS ampliam o uso das informações gerenciais e se dividem em:</p>

<ul>
  <li>
    <strong>SAD (Sistemas de Apoio à Decisão):</strong> apoiam análises e decisões
    gerenciais por meio de simulações e cenários.
  </li>
  <li>
    <strong>SAE (Sistemas de Apoio Executivo):</strong> voltados ao suporte das decisões
    estratégicas da alta gestão.
  </li>
</ul>

<p>
  Esses sistemas utilizam dados históricos combinados com eventos simulados,
  permitindo avaliar possíveis resultados em diferentes cenários
  organizacionais.
</p>

<h3>Aplicações dos Sistemas de Informação Gerencial (SIG)</h3>

<p>
  Os SIGs são aplicados em diversas áreas da organização para apoiar o controle,
  a análise e a tomada de decisões, sempre com base em dados confiáveis.
</p>

<h4>SIG para finanças</h4>

<p>
  Registra e organiza as movimentações financeiras, como entradas e saídas de
  recursos. Gera relatórios que permitem analisar resultados, necessidades de
  recursos e a situação financeira da organização.
</p>

<h4>SIG para vendas e relacionamento com clientes (CRM)</h4>

<p>
  O CRM é um SIG voltado ao gerenciamento do relacionamento com o cliente.
  Permite acompanhar ações realizadas, identificar demandas específicas e
  analisar informações por cliente individual ou por grupos.
</p>

<h4>SIG para produção (manufatura)</h4>

<p>
  Auxilia no controle das demandas por matéria-prima, uso e disponibilidade de
  equipamentos, alocação de mão de obra e gestão das instalações produtivas.
  Apoia o planejamento e a execução eficiente da produção.
</p>

<h4>SIG para gestão de pessoas</h4>

<p>
  Vai além da folha de pagamento, abrangendo informações sobre colaboradores,
  suas demandas e desempenho, apoiando a gestão e a avaliação dos trabalhadores.
</p>

<h4>SIG para gestão de projetos</h4>

<p>
  Permite acompanhar a aplicação dos recursos nos projetos, comparando o
  planejamento com a execução das atividades e apoiando o controle dos
  resultados.
</p>

<h4>Propósito dos SIGs</h4>

<p>
  Os SIGs têm como objetivo melhorar a coleta de dados, ampliar a geração de
  informações e possibilitar o uso eficiente dessas informações para alcançar
  melhores resultados organizacionais.
</p>
