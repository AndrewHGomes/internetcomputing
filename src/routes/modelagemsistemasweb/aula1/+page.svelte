<h2>Modelagem de Sistemas e Processos de Negócio</h2>

<Audios modulo="M8" aulas={["01", "02", "03", "04"]} />

<p>
  A <strong>Modelagem de Sistemas</strong> é a disciplina que traduz as necessidades
  de negócio de um cliente em um projeto técnico de software por meio de modelos
  e documentações. Seu principal objetivo é o planejamento técnico prévio à codificação,
  garantindo que o desenvolvimento seja organizado e atenda fielmente aos objetivos
  esperados.
</p>

<p>
  Um <strong>Processo de Negócio</strong> é definido como um conjunto estruturado
  de tarefas relacionadas que visam produzir um serviço ou produto específico para
  um cliente. Toda organização opera através desses processos, que agrupam atividades
  executadas por diversas pessoas e departamentos em diferentes momentos.
</p>

<p>
  Essencialmente, o processo funciona como um fluxo sequenciado de tarefas que
  utiliza insumos (entradas), realiza o processamento dessas informações ou
  materiais e gera um resultado final (saída). O mapeamento desses fluxos
  permite identificar departamentos envolvidos, normas, regras e procedimentos
  necessários para que a entrega seja eficiente, rápida e livre de tarefas
  redundantes.
</p>

<h4>Exemplos de Atividades de Processo</h4>

<ul>
  <li>Atendimento de pedidos de compras.</li>
  <li>Contratação de funcionários ou fornecedores.</li>
  <li>Desenvolvimento e fabricação de produtos.</li>
  <li>Logística, faturamento e controle de qualidade.</li>
</ul>

<h3>Documentação de Processos de Negócio</h3>

<p>
  A formalização dos processos de negócio é o que organiza o funcionamento de
  uma empresa, servindo como a execução prática de sua estratégia, missão e
  valores. Processos bem definidos são considerados diferenciais competitivos,
  pois garantem que a qualidade do produto ou serviço seja consistente, criando
  valor direto para o cliente.
</p>

<p>
  Quando os processos não são documentados, o conhecimento fica restrito à
  memória dos colaboradores. A modelagem clara e acessível permite a
  padronização dos resultados, possibilitando que diferentes profissionais sigam
  o mesmo passo a passo e atinjam a mesma qualidade esperada. O exemplo clássico
  é a rede McDonald's, onde o processo rigoroso garante que o produto tenha o
  mesmo sabor em qualquer unidade.
</p>

<p>
  A ausência de formalização gera riscos significativos, como a perda de
  conhecimento intelectual em casos de rotatividade de funcionários e a
  dificuldade em implementar melhorias. Além disso, falhas em processos mal
  definidos podem resultar em prejuízos financeiros e danos à imagem da marca,
  como erros de fabricação que exigem recalls custosos.
</p>

<h4>Importância da Formalização</h4>

<ul>
  <li>
    Garante a continuidade do conhecimento, independente da troca de
    funcionários.
  </li>
  <li>Permite a identificação de gargalos para melhoria contínua.</li>
  <li>Assegura a previsibilidade do resultado final para o consumidor.</li>
  <li>
    Facilita o treinamento de novos membros da equipe através de instruções
    claras.
  </li>
</ul>

<h3>Identificação do Processo AS IS</h3>

<p>
  O mapeamento de processos inicia-se com a identificação das atividades, o que
  pode ser complexo por envolver múltiplos departamentos e profissionais. Para
  identificar um processo com precisão, é necessário reconhecer que ele é uma
  organização estruturada de tarefas e recursos voltada a um objetivo global e
  orientada ao cliente final.
</p>

<p>
  O conceito de <strong>AS IS</strong> (traduzido como "como é") refere-se ao mapeamento
  da situação atual do processo. Ele funciona como uma "fotografia" fiel da realidade,
  documentando o passo a passo exatamente como ele é executado no momento presente,
  sem considerar melhorias ou idealizações.
</p>

<p>
  Para realizar esse levantamento de forma fidedigna, utiliza-se a observação
  direta da execução das tarefas e a realização de entrevistas com todos os
  profissionais envolvidos. O objetivo é capturar a visão completa de todas as
  interações e sequências para garantir que o fluxo documentado corresponda ao
  dia a dia da operação.
</p>

<h4>Características de um Processo</h4>

<ul>
  <li>Junção de atividades e recursos com um objetivo global.</li>
  <li>Orientação ao cliente final e execução recorrente para criar hábito.</li>
  <li>
    Necessidade de organização, definição de responsabilidades e desempenho.
  </li>
  <li>Estrutura baseada em entradas, processamento e saídas.</li>
</ul>

<h3>Proposta do Processo TO BE</h3>

<p>
  Enquanto o AS IS retrata o presente, a técnica <strong>TO BE</strong> ("como será")
  foca na proposição de melhorias, correções de falhas e inovação. O objetivo é redesenhar
  o fluxo de trabalho para torná-lo mais ágil e eficiente, resultando em uma nova
  documentação que servirá como guia para o futuro da operação.
</p>

<p>
  Para um TO BE bem-sucedido, é essencial a participação de profissionais que
  contribuam com uma visão inovadora, evitando o apego a métodos antigos. As
  melhorias devem ter um propósito claro e ser implementadas em blocos
  controlados, garantindo que o custo de redesenho e treinamento traga um
  retorno real para o negócio.
</p>

<p>
  O redesenho deve buscar a simplificação máxima, eliminando tarefas redundantes
  e focando no que gera valor. Após a definição do novo fluxo, é necessário
  documentar responsabilidades, estabelecer um cronograma de implementação e
  definir métricas para avaliar se os ganhos esperados (produtividade, redução
  de custos e qualidade) foram atingidos.
</p>

<h4>Questionamentos para o Redesenho</h4>

<p>
  Para estimular a criatividade e identificar melhorias, deve-se questionar:
</p>

<ul>
  <li>O que é feito que não agrega valor ou o que falta para agregar valor?</li>
  <li>O que é feito além do esperado ou de forma ineficaz?</li>
  <li>O que pode ser eliminado, substituído, adicionado ou melhorado?</li>
  <li>Quais tarefas podem ser unificadas ou executadas em paralelo?</li>
</ul>

<h4>Benefícios do Novo Mapeamento</h4>

<ul>
  <li>Facilita o treinamento de novos profissionais.</li>
  <li>Estimula a cultura de melhoria contínua na empresa.</li>
  <li>Garante clareza sobre os ganhos reais de produtividade e qualidade.</li>
</ul>

<h3>Utilizando o BPMN para Modelagem</h3>

<p>
  Para que a gestão de processos seja eficiente, é indispensável o uso de uma
  linguagem única e compreensível por todos os envolvidos. Essa linguagem é a
  <strong>BPMN</strong> (Business Process Model and Notation), a notação oficial
  da disciplina
  <strong>BPM</strong> (Business Process Management). O BPMN permite visualizar o
  encadeamento de atividades, a ordem de execução e as tomadas de decisão de forma
  intuitiva.
</p>

<p>
  O objetivo central do BPMN é refletir fielmente a realidade do dia a dia
  organizacional. Através de elementos visuais padronizados, o fluxo demonstra
  onde um processo começa, quais caminhos ele percorre e como ele termina. Essa
  clareza é o que permite identificar falhas e, fundamentalmente, definir quais
  partes do processo podem ser <strong>automatizadas por um software</strong>,
  tornando-se a base da modelagem de sistemas.
</p>

<h4>Elementos Básicos da Notação</h4>

<ul>
  <li>
    <strong>Eventos (Círculos):</strong> Indicam o início (verde) ou o fim (vermelho)
    de um processo. Um fluxo pode ter múltiplos pontos de partida ou encerramento.
  </li>
  <li>
    <strong>Atividades ou Tarefas (Retângulos):</strong> Representam o trabalho efetivo
    realizado em uma etapa. São organizadas de forma sequencial.
  </li>
  <li>
    <strong>Fluxo de Sequência (Setas):</strong> Linhas sólidas que conectam um elemento
    ao próximo, direcionando o caminho lógico do processo sem ambiguidades.
  </li>
  <li>
    <strong>Gateway (Losango):</strong> Representa pontos de decisão ou desvios lógicos.
    É o elemento usado para separar ou unir diferentes caminhos baseados em condições.
  </li>
</ul>

<h3>Conclusão: O Papel da Modelagem de Processos</h3>

<p>
  A modelagem de processos de negócio é a base estratégica para a construção de
  qualquer software. Ela confere identidade à empresa e organiza o trabalho dos
  profissionais, garantindo que o conhecimento operacional seja
  institucionalizado e não se perca com a rotatividade de funcionários.
</p>

<p>
  O ciclo entre entender a realidade atual (<strong>AS IS</strong>) e projetar a
  melhoria futura (<strong>TO BE</strong>) é o que viabiliza ganhos reais de
  produtividade e qualidade. Ao documentar esses fluxos, a empresa cria a
  clareza necessária para decidir o que deve ser automatizado. Sem esse
  entendimento prévio do negócio, o desenvolvimento de software carece de
  direção e propósito.
</p>

<h4>Pontos Fundamentais</h4>

<ul>
  <li>
    <strong>Propósito:</strong> Traduzir necessidades de negócio em soluções técnicas.
  </li>
  <li>
    <strong>Continuidade:</strong> Evitar a perda de capital intelectual e facilitar
    treinamentos.
  </li>
  <li>
    <strong>Eficiência:</strong> Identificar falhas e gargalos antes da automação.
  </li>
  <li>
    <strong>Fundação:</strong> A modelagem de processos é o "passo zero" da engenharia
    de software.
  </li>
</ul>
