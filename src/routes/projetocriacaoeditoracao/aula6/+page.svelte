<h2>Introdução à Montagem de Sites e WordPress</h2>

<Audios modulo="M5" aulas={["21", "22", "23", "24"]} />

<p>
  Para montar um site de forma eficiente, é essencial ter o projeto consolidado
  e todos os materiais (textos, vídeos, áudios e fotos) organizados em uma pasta
  local. Embora um site seja uma entidade dinâmica que cresce com o tempo, o
  planejamento inicial evita retrabalho. Além do conteúdo, a estrutura depende
  de dois pilares já estudados: o <strong>domínio</strong> (endereço) e a
  <strong>hospedagem</strong>
  (servidor), que geralmente envolvem custos financeiros para as empresas.
</p>

<p>
  Para fins de aprendizado e testes, existem plataformas que operam no modelo
  Freemium — serviços gratuitos com funcionalidades avançadas pagas. Exemplos
  populares incluem Wix, Site123 e Webnode.
</p>

<h3>A Escolha do WordPress</h3>

<p>
  O WordPress é a ferramenta selecionada para este estudo por ser a líder
  absoluta do mercado, utilizada em aproximadamente <strong>
    45% dos sites de todo o mundo
  </strong>. Criada em 2003 originalmente para a gestão de blogs, a plataforma
  evoluiu para um sistema robusto e versátil, mantendo o foco na simplicidade de
  criação e divulgação de conteúdo. As principais características para o início
  do trabalho são:
</p>

<ul>
  <li>
    <strong>Subdomínio Gratuito:</strong> Na versão sem custos, o endereço do site
    será composto pelo nome escolhido seguido de "https://www.google.com/search?q=.wordpress.com".
  </li>
  <li>
    <strong>Requisito de Acesso:</strong> É obrigatório possuir uma conta de e-mail
    ativa para criar o cadastro e realizar o login no sistema.
  </li>
  <li>
    <strong>Escalabilidade:</strong> O conhecimento adquirido na versão gratuita
    é aplicável em ambientes profissionais e servidores próprios.
  </li>
</ul>

<h4>Preparação para o Início</h4>

<p>
  Antes de acessar a plataforma, certifique-se de que a estrutura básica do site
  está definida. O domínio e a hospedagem gratuitos servirão como laboratório
  para aplicar as técnicas de editoração web, fotografia e audiovisual vistas
  anteriormente.
</p>

<h3>Configurações Iniciais no WordPress</h3>

<p>
  O processo de criação começa no site oficial do WordPress. Ao clicar em
  <strong>"Comece agora"</strong>, o usuário deve realizar o cadastro utilizando
  uma conta de e-mail ou perfis de redes sociais (Google, Apple ou GitHub). É
  fundamental estar atento às tentativas da plataforma de oferecer planos pagos,
  garantindo sempre a seleção das opções gratuitas. As etapas principais de
  configuração são:
</p>

<ul>
  <li>
    <strong>Escolha do Domínio:</strong> Deve-se optar explicitamente pelo subdomínio
    gratuito (exemplo: https://www.google.com/search?q=seunome.wordpress.com).
  </li>
  <li>
    <strong>Seleção de Plano:</strong> Mesmo após escolher o domínio, o sistema oferecerá
    planos pagos; selecione novamente a opção de plano gratuito para prosseguir sem
    custos.
  </li>
  <li>
    <strong>Objetivo do Site:</strong> O sistema solicita a finalidade da página
    (vendas, escrita, promoção, etc.). A opção
    <strong>"Promover minha pessoa ou empresa"</strong>
    é uma das mais comuns para perfis profissionais.
  </li>
</ul>

<h4>Estrutura e Design</h4>

<p>
  Após as configurações iniciais, o WordPress oferece duas abordagens para o
  design do site:
</p>

<ol>
  <li>
    <strong>Escolher um Tema (Modelo):</strong> Oferece um design pronto e visualmente
    harmônico. É a escolha ideal para iniciantes que desejam se familiarizar com
    a ferramenta rapidamente.
  </li>
  <li>
    <strong>Crie você mesmo:</strong> Opção que permite maior controle sobre a identidade
    visual. Ao selecionar esta alternativa, um menu lateral é exibido com todas as
    configurações de personalização.
  </li>
</ol>

<p>
  Nesse menu de criação própria, é possível definir o <strong>
    layout de cada página individualmente
  </strong>, como a página inicial (entrada), a seção de serviços e o espaço
  para depoimentos, permitindo uma construção personalizada de acordo com as
  necessidades do projeto.
</p>

<h3>Identidade Visual e Estrutura de Páginas</h3>

<p>
  Após definir os layouts, o WordPress direciona o usuário para a etapa de
  <strong>"Selecionar estilos"</strong>, onde a identidade visual do projeto é
  consolidada por meio de cores e tipografia.
</p>

<ul>
  <li>
    <strong>Paleta de Cores:</strong> O sistema oferece combinações de cores pré-definidas
    e harmoniosas. Escolher uma dessas paletas garante que o site mantenha um equilíbrio
    visual profissional sem esforço técnico excessivo.
  </li>
  <li>
    <strong>Fontes (Tipografia):</strong> Da mesma forma que as cores, existem conjuntos
    de fontes que combinam entre si. A escolha correta dos tipos de letra é essencial
    para a legibilidade e para transmitir a personalidade do projeto.
  </li>
</ul>

<h4>Finalização e Conteúdo</h4>

<p>
  Com a estética definida, o sistema entra na fase final de montagem estrutural,
  onde você deve decidir a árvore de páginas do seu site:
</p>

<ol>
  <li>
    <strong>Adição de Páginas:</strong> Você pode incluir seções específicas como
    Galeria, Loja, Blog ou Serviços.
  </li>
  <li>
    <strong>Confirmação:</strong> Após selecionar as páginas desejadas, clique
    em
    <strong>"Salvar e Continuar"</strong>.
  </li>
  <li>
    <strong>Início da Edição:</strong> O botão
    <strong>"Comece a adicionar conteúdo"</strong> finaliza a configuração e abre
    o editor propriamente dito.
  </li>
</ol>

<p>
  Ao concluir a construção, o WordPress disponibiliza um <strong
    >tour guiado</strong
  > pelo sistema no menu esquerdo. Embora a interface seja intuitiva, realizar esse
  tour é recomendável para localizar rapidamente as ferramentas de edição e gerenciamento
  que serão usadas no dia a dia.
</p>

<h3>Edição e Personalização de Conteúdo</h3>

<p>
  Com as páginas estruturadas, o WordPress permite a edição de qualquer elemento
  visual ou textual. O processo de customização é baseado na seleção direta: ao
  clicar em um item específico na visualização do site, um menu de configurações
  correspondente é aberto à direita para ajustes detalhados.
</p>

<ul>
  <li>
    <strong>Edição de Menus:</strong> É possível alterar os títulos dos links, adicionar
    novas páginas ao menu ou remover itens inseridos automaticamente pelo modelo.
  </li>
  <li>
    <strong>Dinâmica de Trabalho:</strong> A construção é feita parte por parte.
    A recomendação é experimentar diferentes combinações e alterações, sempre
    lembrando de clicar em <strong>"Salvar"</strong> para não perder o progresso.
  </li>
</ul>

<h4>Navegação entre Páginas e Painel</h4>

<p>
  Para gerenciar as diferentes seções do site e acessar as configurações
  administrativas, utiliza-se o logotipo do WordPress no canto superior
  esquerdo:
</p>

<ul>
  <li>
    <strong>Troca de Páginas:</strong> Ao clicar no logotipo, o sistema exibe a lista
    de todas as páginas criadas. Para editar uma página específica, basta clicar
    com o botão direito sobre ela e selecionar a opção para ir até aquela página.
  </li>
  <li>
    <strong>Retorno ao Painel (Dashboard):</strong> O logotipo também serve como
    atalho para retornar ao painel de controle principal. O
    <strong>Dashboard</strong> é a central de comando onde se tem a visão geral do
    site e o acesso às ferramentas avançadas de utilização e manutenção.
  </li>
</ul>

<h3>Dashboard e Gerenciamento de Conteúdo</h3>

<p>
  O <strong>Dashboard</strong> (painel de controle) é a central administrativa do
  WordPress. Nele, você encontra todas as ferramentas para configurar o site, além
  de gerenciar páginas e postagens.
</p>

<ul>
  <li>
    <strong>Criar novas páginas:</strong> Acesse o menu "Páginas" e clique em "Adicionar
    nova página". O sistema oferecerá novamente opções de layouts para facilitar
    o design.
  </li>
  <li>
    <strong>Editar páginas existentes:</strong> No menu "Páginas", basta selecionar
    a página desejada e clicar diretamente sobre o conteúdo para começar a escrever.
  </li>
</ul>

<h4>Inserção de Elementos e Mídias</h4>

<p>
  A personalização do conteúdo é feita através do sistema de blocos. Ao clicar
  no ícone de "+" no lado direito, abre-se um menu de inserção rápida. Para
  visualizar todas as possibilidades, clique em <strong
    >"Navegar por todos"</strong
  >, o que abrirá um menu lateral com diversas opções:
</p>

<ul>
  <li>
    <strong>Mídias:</strong> Imagens, galerias de fotos, vídeos e arquivos.
  </li>
  <li>
    <strong>Widgets:</strong> Ferramentas funcionais como calendários e ícones de
    redes sociais.
  </li>
  <li>
    <strong>Tecnologia:</strong> Assistentes de inteligência artificial para auxiliar
    na criação.
  </li>
</ul>

<h4>Salvar Rascunho vs. Publicar</h4>

<p>
  No WordPress, existe uma distinção crucial entre salvar o trabalho e
  disponibilizá-lo online:
</p>

<ul>
  <li>
    <strong>Salvar Rascunho:</strong> Armazena suas alterações de forma privada.
    Apenas você consegue visualizar o conteúdo, permitindo pausas e revisões contínuas
    sem exposição externa.
  </li>
  <li>
    <strong>Publicar:</strong> Torna a página acessível globalmente na internet.
    Uma vez clicado, qualquer pessoa com o link poderá visualizar as informações
    inseridas.
  </li>
</ul>

<p>
  É recomendável revisar todo o conteúdo minuciosamente antes de utilizar a
  opção de publicação definitiva.
</p>

