<h2>Fundamentos: Hardware, Software e Sistemas</h2>

<p>
  O técnico em informática para internet atua na intersecção entre a necessidade
  do cliente e a solução tecnológica. Para isso, é preciso distinguir três
  elementos fundamentais:
</p>

<ul>
  <li>
    <strong>Hardware:</strong>
    É a <strong>parte física</strong> do computador (circuitos, placas, periféricos
    e componentes eletrônicos).
  </li>
  <li>
    <strong>Software:</strong>
    É o componente lógico, composto por <strong>programas de computador</strong>
    (linhas de código),
    <strong>dados</strong> (informações processadas) e
    <strong>documentação associada</strong> (manuais e registros técnicos).
  </li>
  <li>
    <strong>Sistema:</strong>
    É um conceito amplo que representa o <strong>todo unificado</strong>. Um
    sistema de TI envolve a interação entre hardware, software, bancos de dados
    e servidores para atingir um objetivo específico.
  </li>
</ul>

<h3>O Software e seus Requisitos</h3>

<p>
  O desenvolvimento de software pode ser classificado quanto ao seu público-alvo
  em <strong>genérico</strong> (produtos de prateleira vendidos para vários
  clientes) ou
  <strong>customizado</strong> (desenvolvido sob medida para uma necessidade
  específica). Independentemente do tipo, o sucesso do projeto depende dos
  <strong>requisitos</strong>.
</p>

<strong>Requisitos</strong> são as funcionalidades que o software deve executar
(ex: um "carrinho de compras" em um site). Compreender os requisitos exige que o
desenvolvedor conheça o negócio do cliente para agregar valor econômico à
solução.

<h3>Categorias de Software</h3>
<p>Os softwares são agrupados conforme sua finalidade principal:</p>

<ul>
  <li>
    <strong>Básico:</strong>
    Programas de apoio, como os <strong>Sistemas Operacionais</strong>.
  </li>
  <li>
    <strong>Comercial:</strong>
    Focado em gestão e negócios, como <strong>ERP</strong> (Gestão Empresarial)
    e <strong>CRM</strong> (Gestão de Relacionamento).
  </li>
  <li>
    <strong>Científico e de Engenharia:</strong>
    Voltados para cálculos complexos e algoritmos matemáticos.
  </li>
  <li>
    <strong>Embutido/Embarcado:</strong>
    Softwares integrados a hardwares específicos, como os de carros e aviões.
  </li>
  <li>
    <strong>Escritório/Pessoal:</strong>
    Ferramentas de produtividade diária (textos, planilhas, slides).
  </li>
  <li>
    <strong>Inteligência Artificial:</strong>
    Algoritmos que utilizam lógica não numérica para aprendizado e resolução de problemas
    complexos. Permitem à máquina aprender.
  </li>
</ul>

<h3>Terminologias de Mercado e Web e seus Papéis</h3>

<p>
  No dia a dia profissional, é importante distinguir papéis e conceitos que
  parecem semelhantes:
</p>

<ul>
  <li>
    <strong>Técnica vs. Tecnologia:</strong>
    <strong>Técnica</strong> é o conjunto de métodos para executar processos
    (ex: técnicas de programação). <strong>Tecnologia</strong> é a técnica evoluída
    e sistematizada, unindo conhecimento e ferramentas.
  </li>
  <li>
    <strong>Cliente vs. Usuário:</strong>
    <strong>Cliente</strong> é quem contrata e paga pelo serviço.
    <strong>Usuário</strong> é quem efetivamente utiliza a ferramenta no dia a dia.
    O desenvolvedor deve ouvir o usuário para entender o problema real.
  </li>
  <li>
    <strong>Internet</strong>
    Um sistema global de redes interligadas que utiliza o protocolo
    <strong>TCP/IP</strong> para comunicação mundial.
  </li>
  <li>
    <strong>Desenvolvimento Web:</strong>
    Desenvolvimento Web: Criação de sites e sistemas acessíveis via internet. Pode
    ser dividido entre o <strong>Web Designer</strong> (focado no layout/visual)
    e o <strong>Web Developer</strong>
    (focado na lógica e sistemas).
  </li>
</ul>
