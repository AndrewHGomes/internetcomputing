<h2>Introdução e Atuação Profissional</h2>

<Audios modulo="M1" aulas={["01", "02", "03", "04"]} />

<p>
  O curso foca na formação do <strong
    >técnico em informática para internet</strong
  >, capacitando o aluno para atuar tanto na manutenção de infraestrutura quanto
  no <strong>desenvolvimento de software</strong>. O objetivo central é preparar
  o profissional para criar programas que funcionem em navegadores, lidando com
  múltiplos usuários simultâneos e protocolos de
  <strong>segurança digital</strong>.
</p>

<p>
  A <strong>Tecnologia da Informação (TI)</strong> é definida como o conjunto de
  atividades e soluções providas por recursos computacionais para produção,
  armazenamento, transmissão, acesso e segurança das
  <strong>informações</strong>. Por ser uma área vasta, o profissional deve
  estar familiarizado com:
</p>

<ul>
  <li>Informática básica, hardware e redes.</li>
  <li>Lógica de programação e algoritmos.</li>
  <li>Banco de dados e modelagem (web e mobile).</li>
  <li>Gestão de sistemas e negócios digitais.</li>
</ul>

<h3>A Importância da TI na Economia</h3>

<p>
  A TI deixou de ser apenas suporte para se tornar uma <strong
    >ferramenta estratégica</strong
  >
  essencial na tomada de decisão e na geração de diferencial competitivo. Ela promove
  a <strong>transformação digital</strong>, automatizando processos repetitivos
  e tornando as empresas mais ágeis e inteligentes através do
  <strong> mapeamento de processos </strong>.
</p>

<p>
  O mercado de tecnologia é um dos mais promissores do mundo, com crescimento
  constante e alta demanda por mão de obra. No cenário atual, quase todas as
  empresas precisam se tornar "empresas de tecnologia" para sobreviver, o que
  gera um <strong>déficit de profissionais</strong> qualificados em áreas como desenvolvimento
  mobile, computação em nuvem e inteligência artificial.
</p>

<h3>Perfil do Profissional de TI</h3>

<p>
  O profissional moderno precisa ser <strong>versátil</strong>, unindo o
  conhecimento técnico à compreensão dos processos de negócio (logística, RH,
  financeiro, etc.). O perfil exigido pelas empresas evoluiu de um foco
  puramente técnico para a valorização de <strong>soft skills</strong>, como:
</p>

<ul>
  <li>
    <strong>Comunicação assertiva</strong> para entender as demandas dos clientes.
  </li>
  <li>
    <strong>Criatividade</strong> e proatividade na resolução de problemas.
  </li>
  <li>
    <strong>Aprendizado contínuo</strong>, pois a evolução tecnológica exige
    atualização constante.
  </li>
</ul>

<h3>Formação, Regulamentação e Mercado</h3>

<p>
  Existem diferenças fundamentais entre os níveis de formação: o <strong
    >curso técnico</strong
  >
  oferece uma formação rápida, focada e prática para o mercado, enquanto a
  <strong>graduação</strong> (bacharelado ou tecnólogo) aprofunda os conceitos teóricos.
  O curso técnico é frequentemente a porta de entrada para uma carreira sólida.
</p>

<ul>
  <li>
    <strong>Curso Técnico:</strong>
    Focado em formação rápida e específica, com carga horária entre 800 e 1.200 horas.
    É uma excelente porta de entrada para o mercado de trabalho.
  </li>
  <li>
    <strong>Ensino Superior:</strong>
    Oferece maior profundidade teórica e prática. Pode ser Tecnólogo (2 a 3 anos)
    ou Bacharelado/Licenciatura (média de 4 anos).
  </li>
</ul>

<p>
  Atualmente, a profissão de TI não é regulamentada no Brasil, o que significa
  que não há exigência legal de registro em conselhos (como OAB ou CRM) para
  exercer a função. No entanto, há projetos de lei em tramitação para criar
  órgãos de classe. No mercado atual, os profissionais podem atuar via CLT ou
  como autônomos (PJ), sendo a qualificação e a experiência os principais
  diferenciais para obter melhores remunerações.
</p>
