<h2>O que é Programação e Linguagens</h2>

<p>
  <strong>Programação</strong> é o conjunto de instruções lógicas e algoritmos
  que formam um software. Como os computadores operam em
  <strong>linguagem de máquina</strong> (código binário de 0 e 1), as linguagens
  de programação servem como uma interface de "tradução" para o desenvolvedor.
</p>

<p>
  As linguagens podem ser de <strong>baixo nível</strong> (próximas ao código de
  máquina) ou <strong>alto nível</strong> (próximas à linguagem humana). As principais
  utilizadas no mercado são:
</p>

<ul>
  <li>
    <strong>PHP:</strong>
    Focada em sistemas web dinâmicos; é de distribuição livre.
  </li>
  <li>
    <strong>Java:</strong>
    Famosa pela filosofia "escreva uma vez, execute em qualquer lugar"; muito usada
    em sistemas corporativos e Android.
  </li>
  <li>
    <strong>C#:</strong>
    Desenvolvida pela Microsoft para a plataforma .NET.
  </li>
  <li>
    <strong>Python:</strong>
    Linguagem de alto nível (sintaxe simples), ideal para iniciantes, IA e ciência
    de dados.
  </li>
  <li>
    <strong>C++:</strong>
    Evolução da linguagem C, incluindo recursos de orientação a objetos.
  </li>
</ul>

<h3>Paradigmas e Categorias de Programadores</h3>

<p>
  Um <strong>paradigma de programação</strong> é a forma ou metodologia
  utilizada para resolver um problema. Os principais são o
  <strong>Procedural</strong>
  (sequência linear de comandos) e o
  <strong>Orientado a Objetos</strong> (baseado em componentes individuais chamados
  objetos). Quanto à área de atuação, os programadores se dividem em:
</p>

<ul>
  <li>
    <strong>Web:</strong>
    Foco do curso. Cria sistemas complexos acessados via internet, exigindo alta
    segurança e suporte a muitos usuários.
  </li>
  <li>
    <strong>Desktop:</strong>
    Cria softwares para uso local em máquinas ou redes restritas.
  </li>
  <li>
    <strong>Mobile:</strong>
    Desenvolve para dispositivos móveis (Android/iOS), focando em performance e tamanho
    do app.
  </li>
  <li>
    <strong>Jogos:</strong>
    Focado em lógica complexa, estratégias de game e, muitas vezes, habilidades de
    design.
  </li>
</ul>

<h3>O Processo de Desenvolvimento de Software</h3>

<p>
  O desenvolvimento segue um <strong>ciclo de vida</strong> composto por fases essenciais
  para garantir a qualidade do produto:
</p>

<ol>
  <li>
    <strong>Levantamento e Análise de Requisitos:</strong> Entender o problema do
    cliente e definir as regras de negócio (como cálculos e relatórios devem funcionar).
  </li>
  <li>
    <strong>Projeto Técnico:</strong> Planejar a arquitetura, escolher a linguagem
    e o banco de dados.
  </li>
  <li>
    <strong>Codificação:</strong> A fase de escrita do código propriamente dita,
    aplicando lógica de programação e segurança da informação.
  </li>
  <li>
    <strong>Testes:</strong> Verificação crítica para garantir que o sistema faz
    o que foi planejado e está livre de erros.
  </li>
  <li>
    <strong>Implantação:</strong> Colocar o software em "produção", ou seja,
    disponível para o usuário final em servidores ou na
    <strong>nuvem (Cloud Computing).</strong>
  </li>
</ol>

<h3>Conhecimentos Complementares Essenciais</h3>

<p>
  Para ser um desenvolvedor web completo, além de programar, é necessário
  compreender:
</p>

<ul>
  <li>
    <strong>Sistemas Operacionais:</strong>
    Windows, Linux e Mac OS, que gerenciam os recursos do hardware.
  </li>
  <li>
    <strong>Redes e Cloud Computing:</strong>
    Entender como os dados trafegam (protocolos de comunicação) e como hospedar sistemas
    na nuvem (AWS, Azure) sem depender de servidores físicos locais.
  </li>
  <li>
    <strong>SGBD (Banco de Dados):</strong>
    Organizar a informação de forma otimizada para que o software seja eficiente,
    pois os dados são o ativo mais valioso de uma empresa.
  </li>
  <li>
    <strong>Algoritmos:</strong>
    Dominar a sequência finita de ações necessárias para resolver qualquer problema
    lógico.
  </li>
</ul>
