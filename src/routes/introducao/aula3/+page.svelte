<h2>Contexto Histórico e Evolução</h2>

<p>
  A história da TI é marcada por ciclos de inovação que transformaram máquinas
  mecânicas em sistemas globais interconectados.
</p>

<ul>
  <li>
    <strong>Inícios (Séculos XVII a XIX):</strong>
    Destaque para a calculadora de Blaise Pascal (1642), o tear de Jacquard (1802)
    e a máquina analítica de Charles Babbage. Em 1835, Ada Byron Lovelace criou o
    primeiro programa da história, tornando-se a primeira programadora.
  </li>
  <li>
    <strong>1950 – 1965:</strong> O hardware era de propósito geral e a programação
    era vista como uma arte secundária, sem métodos sistemáticos ou documentação.
  </li>
  <li>
    <strong>1965 – 1975:</strong> Surgimento da
    <strong>multiprogramação</strong>, sistemas multiusuários e a 1ª geração de
    Bancos de Dados (<strong>SGBD</strong>). O software passou a ser vendido
    como produto.
  </li>
  <li>
    <strong>1975 – Atualidade:</strong> Consolidação da
    <strong>Internet</strong>, uso massivo de microprocessadores, surgimento do
    <strong>Big Data</strong>, computação móvel e
    <strong>metodologias ágeis</strong>. A década de 60 marcou o nascimento da
    <strong>Engenharia de Software</strong>, visando trazer rigor e qualidade ao
    desenvolvimento.
  </li>
</ul>

<h3>Ética e Responsabilidade Profissional</h3>

<p>
  O técnico em informática lida com dados confidenciais e estratégicos, exigindo
  uma conduta pautada por leis e princípios morais. As diretrizes principais vêm
  de órgãos como <strong>ACM</strong> e <strong>IEEE</strong>.
</p>

<ul>
  <li>
    <strong>Princípios Gerais:</strong> Contribuir para o bem-estar social, evitar
    danos (perda de dados, danos à reputação), ser honesto sobre qualificações e
    respeitar a privacidade e a confidencialidade.
  </li>
  <li>
    <strong>Direitos Autorais:</strong> Respeitar patentes e licenças de software,
    valorizando o trabalho criativo.
  </li>
  <li>
    <strong>Responsabilidade Técnica:</strong> Realizar apenas trabalhos para os
    quais possui competência e manter-se em atualização contínua.
  </li>
  <li>
    <strong>Acesso Autorizado:</strong> Jamais acessar sistemas ou dados sem permissão,
    exceto em casos excepcionais para evitar danos maiores.
  </li>
</ul>

<h3>Principais Funções e Atribuições no Mercado</h3>

<p>A área de TI oferece diversas especializações além da programação pura:</p>

<ul>
  <li>
    <strong>Desenvolvedor/Programador:</strong> Especialista em escrever e manter
    códigos em linguagens específicas.
  </li>
  <li>
    <strong>Analista de Sistemas:</strong>
    Responsável por entender as regras de negócio e projetar a solução estratégica
    para o desenvolvedor.
  </li>
  <li>
    <strong>Arquiteto de Software:</strong>
    Planeja a estrutura de alto nível e a integração dos componentes do sistema.
  </li>
  <li>
    <strong>Testador (QA):</strong>
    Garante a qualidade e a ausência de erros antes da entrega ao cliente.
  </li>
  <li>
    <strong>DBA (Gestor de Dados):</strong>
    Cuida da instalação, segurança e performance dos dados.
  </li>
  <li>
    <strong>Gestor de Projetos:</strong>
    Planeja e controla prazos, custos e equipes (métodos tradicionais ou ágeis).
  </li>
  <li>
    <strong>Analista de Segurança:</strong>
    Protege os dados contra ameaças e garante conformidade com leis como a LGPD.
  </li>
</ul>

<h3>Profissões em Alta e Novas Tecnologias</h3>

<p>
  O mercado enfrenta um "apagão técnico" (déficit de profissionais), elevando a
  busca por perfis especializados:
</p>

<ul>
  <li>
    <strong>Desenvolvimento Mobile e UX:</strong>
    Focado em aplicativos, UX (experiência do usuário) e metodologias ágeis.
  </li>
  <li>
    <strong>Cloud Computing:</strong>
    Gestão de infraestrutura em plataformas como <strong>AWS</strong> e
    <strong>Azure</strong>.
  </li>
  <li>
    <strong>Ciência de Dados e IA:</strong>
    Uso de algoritmos e aprendizado de máquina para transformar grandes volumes de
    dados (<strong>Big Data</strong>) em decisões.
  </li>
  <li>
    <strong>IoT (Internet das Coisas):</strong>
    Conexão digital de objetos do cotidiano à rede.
  </li>
  <li>
    <strong>DevOps:</strong>
    Integração entre as equipes de desenvolvimento e operações para garantir que
    o software funcione perfeitamente no ambiente real.
  </li>
</ul>
