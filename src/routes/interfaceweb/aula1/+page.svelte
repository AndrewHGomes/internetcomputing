<p>
  Nosso estudo aborda a história da internet, técnicas de fotografia, elementos
  de design de páginas web e sua disposição para otimizar a navegação do
  usuário.
</p>

<h4>O que é interface web?</h4>

<ul>
  <li>
    Interface é tudo que percebemos e podemos manipular, seja fisicamente ou
    digitalmente; é a camada de comunicação entre usuário e criador (Norman,
    2006).
  </li>
  <li>
    Funciona como tradutora, mediando a interação e permitindo que cada parte
    compreenda a outra. É uma relação semântica, focada em significado e
    expressão (Johnson, 2001).
  </li>
  <li>
    <strong>Princípio central:</strong> <strong>visibilidade</strong> — instruções,
    ícones e mecanismos de ação devem estar claros e evidentes ao usuário.
  </li>
</ul>

<h4>Comunicação e limitação das interfaces</h4>

<ul>
  <li>
    Na interação face a face, sinais não verbais (expressões, gestos, olhar)
    ajudam a entender sentimentos e intenções (Primo, 2011).
  </li>
  <li>
    Interfaces digitais estreitam essa comunicação simbólica, pois telas reduzem
    informações não verbais.
  </li>
  <li>
    Esse estreitamento é mais intenso na internet do que em meios anteriores,
    como cartas (resposta lenta) ou telefone (entonação presente).
  </li>
</ul>

<h4>Importância histórica</h4>

<p>
  Compreender a origem da internet ajuda a entender como o conceito e o design
  de interfaces evoluíram ao longo do tempo.
</p>

<h3>Antes da Internet</h3>

<p>
  O mundo digital não surgiu do nada; antes da revolução digital, a escrita já
  permitia a comunicação entre pessoas separadas no espaço e no tempo, como nos
  textos de Shakespeare. A evolução digital, ao contrário da escrita, é bem
  documentada e tem início na Segunda Guerra Mundial.
</p>

<h4>Principais marcos do digital</h4>

<ul>
  <li>
    <strong>Alan Turing:</strong> Desenvolveu métodos para decifrar mensagens criptografadas
    e formalizou o conceito de algoritmo em 1936, definindo-o como um conjunto ordenado
    e não ambíguo de passos para atingir um resultado.
  </li>
  <li>
    <strong>Vannevar Bush:</strong> Idealizou o memex em 1945, máquina que armazenaria
    livros, registros e comunicações com rapidez e flexibilidade, funcionando como
    um “suplemento da memória”.
  </li>
  <li>
    <strong>Primeiros computadores:</strong> O ENIAC, nos EUA, era enorme (46 m de
    comprimento, 3 m de altura) e utilizado em cálculos para programas espaciais.
  </li>
</ul>

<h4>Microcomputadores e popularização</h4>

<p>
  Na década de 1970, Steve Jobs e Steve Wozniak fundam a Apple Computers e
  lançam o Apple II, primeiro computador pessoal. Mais tarde, na década de 1980,
  computadores, relógios digitais, agendas eletrônicas, brinquedos com chips e
  carros com injeção eletrônica entram nos lares, tornando o digital acessível
  ao público geral.
</p>

<p>
  Todos esses avanços prepararam o terreno para a maior invenção do mundo
  digital: <strong>a internet</strong>.
</p>

<h3>Origem da Internet</h3>

<p>
  A internet surgiu durante a <strong>Guerra Fria</strong> como uma arma de
  informação entre EUA e URSS. Em 1969, os EUA financiaram a
  <strong>ARPA</strong>, que conectou quatro computadores em universidades da
  Califórnia e Utah, formando a Arpanet. Em 1973, o governo ampliou a
  <strong>Arpanet</strong>
  para cerca de 100 computadores e contratou <strong>Vinton Cerf</strong>, que
  criou o <strong>protocolo TCP/IP</strong>, permitindo que computadores se
  conectassem via modem — nascimento da internet.
</p>

<h4>Popularização e evolução</h4>

<p>
  Baseada no conceito de hipertexto de Ted Nelson (1965), a rede se popularizou
  nos anos 1980, mas era apenas texto simples em telas monótonas.
</p>

<ul>
  <li>
    <strong>Tim Berners-Lee</strong> criou a
    <strong>World Wide Web (WWW)</strong>
    no <strong>CERN</strong>, introduzindo <strong>interface gráfica</strong>,
    imagens, áudio e vídeo, tornando a internet mais visual e interativa.
  </li>
  <li>
    <strong>Marc Andreessen</strong> desenvolveu o navegador
    <strong>Mosaic</strong>, e depois fundou a <strong>Netscape</strong>,
    permitindo que qualquer pessoa navegasse na web a partir de 1993.
  </li>
</ul>

<h4>Crescimento e impacto social</h4>

<p>
  Entre 1994 e 1998, a internet se difundiu rapidamente em famílias de classe
  média no mundo todo, incluindo o Brasil, especialmente após o <strong
    >Plano Real</strong
  >. O
  <strong>e-mail</strong> substituiu em grande parte a correspondência em papel,
  transformando serviços de correios. A internet cresceu com
  <strong> e-mails, chats, animações e websites </strong> variados, até a crise
  do
  <strong>estouro da bolha .com</strong> em 2000, que eliminou muitas empresas e
  deixou o setor mais focado e útil para os consumidores.
</p>

<h3>Internet Ubíqua</h3>

<p>
  A internet se tornou <strong>onipresente</strong>, presente em todos os
  lugares e momentos, trazendo benefícios e desafios.
</p>

<h4>Benefícios</h4>

<p>Acesso instantâneo a informações, entretenimento e obras culturais:</p>

<ul>
  <li><strong>Músicas:</strong> Spotify, Deezer, Tidal</li>
  <li><strong>Filmes e vídeos:</strong> YouTube, Netflix</li>
  <li>
    <strong>Acervos de museus:</strong> obras de Da Vinci, Dalí, Tarsila do Amaral,
    entre outros
  </li>
</ul>

<p>
  A <strong>conexão global</strong> tornou possível que pessoas distantes pudessem
  interagir instantaneamente.
</p>

<h4>Desafios e dependência</h4>

<p>
  A dependência de dispositivos móveis é alta; perder o smartphone é considerado
  um grande problema. Jovens passam cerca de <strong>9 horas conectados</strong>
  diariamente (Datareportal, 2022).
</p>

<h4>Tecnologias que possibilitam a ubiquidade</h4>

<ul>
  <li>
    <strong>Dados móveis:</strong> pacote de internet via operadoras (Claro, Oi,
    Tim, Vivo) acessado pelo celular.
  </li>
  <li>
    <strong>Wi-Fi:</strong> acesso fixo à internet na residência, distribuído sem
    fios para diversos dispositivos.
  </li>
</ul>

<p>
  Ambos existiam antes dos smartphones, mas os smartphones transformaram
  radicalmente a vida pessoal e os negócios.
</p>

<h3>Smartphones: Evolução e Impacto</h3>

<p>
  <strong>Conceito:</strong> smartphone = telefone inteligente; evolução do telefone
  celular, que antes era fixo e ligado a um local, não a uma pessoa.
</p>

<h4>Histórico:</h4>

<ul>
  <li><strong>1876:</strong> primeira patente de telefone.</li>
  <li>
    <strong>1973:</strong> Motorola apresenta o primeiro celular portátil; vendas
    só começam em 1983, caro e acessível apenas a poucos.
  </li>
  <li>
    <strong>2002–2007:</strong> celulares começam a se aproximar de mini-computadores,
    com câmeras, telas coloridas e e-mails.
  </li>
</ul>

<h4>Revolução do iPhone (2007)</h4>

<p>
  Apple, liderada por Steve Jobs, cria o <strong>iPhone</strong>, primeiro
  smartphone moderno com <strong>multitouch</strong> (toques múltiplos na tela).
  O conceito mudou. O celular deixa de ser apenas telefone, teclado físico desaparece,
  e a interface é quase toda virtual.
</p>

<h4>Android e sistemas operacionais</h4>

<p>
  Em 2008, A Google lança Android, sistema operacional baseado em Linux, de
  código aberto, que qualquer fabricante pode usar.
</p>

<ul>
  <li>
    <strong>Comparação atual:</strong> <strong>Android</strong> = 71,31% dos
    celulares; <strong>iOS</strong> = 27,95%;
    <strong>outros</strong> = 0,74% (Statcounter, 2024).
  </li>
  <li>
    iPhone integra hardware e sistema (iOS); Android fornece apenas o sistema
    para fabricantes.
  </li>
</ul>

<h4>Função e importância atual</h4>

<p>
  Smartphones não são mais apenas telefones, se tornaram companheiros
  essenciais, com múltiplas funções:
</p>

<ul>
  <li>
    Agenda, calculadora, câmera, música, banco, TV, lojas, mapas, transporte,
    entretenimento, controle de dispositivos domésticos, comunicação, entre
    outras.
  </li>
  <li>
    São dispositivos intuitivos, praticamente sem necessidade de manuais
    extensos, e fundamentais na vida cotidiana, junto com a internet.
  </li>
</ul>

